import type {
	AgentDef,
	BuildContext,
	OutputFile,
	ToolAdapter,
} from "@sdlc-agents/core";

const MODEL_MAP: Record<string, string> = {
	fast: "claude-haiku-4-5-20251001",
	balanced: "claude-sonnet-4-6",
	"high-reasoning": "claude-opus-4-8",
};

// Agent YAML declares tools with abstract, tool-agnostic names. Claude Code's
// subagent runtime restricts a subagent to exactly the names listed in its
// `tools:` frontmatter, so these must be translated to real Claude Code tool
// names or the subagent can't act at all. Names absent from this map (e.g. a
// future/custom tool) pass through unchanged rather than being dropped.
const TOOL_MAP: Record<string, string | string[]> = {
	read_file: "Read",
	write_file: ["Write", "Edit"],
	list_files: "Glob",
	grep: "Grep",
	bash: "Bash",
	git_diff: "Bash",
};

function mapTools(toolsRequired: string[]): string[] {
	const out = new Set<string>();
	for (const t of toolsRequired) {
		const mapped = TOOL_MAP[t] ?? t;
		for (const m of Array.isArray(mapped) ? mapped : [mapped]) out.add(m);
	}
	return [...out];
}

// Support files (templates + policies) are bundled under this directory so that
// copying the whole `.claude/` folder into another repo is self-contained — the
// agent files reference these paths, not the root-level templates/ and policies/.
const BUNDLE_DIR = ".claude/sdlc";

export class ClaudeCodeAdapter implements ToolAdapter {
	readonly name = "claude-code";

	render(agents: AgentDef[], ctx: BuildContext): OutputFile[] {
		const agentFiles = agents.map((a) => ({
			path: `.claude/agents/${a.id}.md`,
			content: this.renderAgent(a),
		}));
		// One slash command per agent — an explicit, discoverable entry point that
		// dispatches to the subagent of the same name. The subagent file holds the
		// workflow; the command is the user-facing trigger (`/requirement-analyst …`).
		const commandFiles = agents.map((a) => ({
			path: `.claude/commands/${a.id}.md`,
			content: this.renderCommand(a),
		}));
		// Bundle every referenced template + policy so `.claude/` stands alone.
		const supportFiles = this.renderSupportFiles(agents, ctx);
		return [...agentFiles, ...commandFiles, ...supportFiles];
	}

	/**
	 * Emit each template/policy referenced by any agent into the bundle dir.
	 * Referenced-only (not the whole templates//policies/ dirs) keeps the bundle
	 * minimal; sorted + deduped to stay deterministic. Files absent from the
	 * BuildContext (e.g. unit tests passing empty maps) are silently skipped —
	 * the agent reference still points at the bundle path it will live at.
	 */
	private renderSupportFiles(
		agents: AgentDef[],
		ctx: BuildContext,
	): OutputFile[] {
		const templateFiles = new Set<string>();
		const policyFiles = new Set<string>();
		for (const a of agents) {
			if (a.output_template) templateFiles.add(basename(a.output_template));
			for (const p of a.policies) policyFiles.add(`${p}.md`);
		}

		const out: OutputFile[] = [];
		for (const file of [...templateFiles].sort()) {
			const content = ctx.templates.get(file);
			if (content !== undefined) {
				out.push({ path: `${BUNDLE_DIR}/templates/${file}`, content });
			}
		}
		for (const file of [...policyFiles].sort()) {
			const content = ctx.policies.get(file);
			if (content !== undefined) {
				out.push({ path: `${BUNDLE_DIR}/policies/${file}`, content });
			}
		}
		return out;
	}

	private renderCommand(agent: AgentDef): string {
		const description = `[SDLC:${agent.phase}] ${agent.description
			.trim()
			.replace(/\n/g, " ")}`;
		return `---
description: >-
  ${description}
---

Use the **${agent.id}** subagent (SDLC ${agent.phase} phase) to handle the request below.

$ARGUMENTS

The subagent is defined in \`.claude/agents/${agent.id}.md\`. Follow its workflow end to end, produce its output artifact (if it declares an output template), and finish with the handoff block to the next phase.

---
_Generated by sdlc-agents. Do not edit — run \`sdlc build\` to regenerate._
`;
	}

	private renderAgent(agent: AgentDef): string {
		const tools = mapTools(agent.tools_required);
		const toolsYaml = tools.length
			? tools.map((t) => `  - ${yamlQuoted(t)}`).join("\n")
			: "  []";

		const claudeNote = agent.model_variants?.claude?.prompt_append
			? `\n> **Claude-specific:** ${agent.model_variants.claude.prompt_append}\n`
			: "";

		const inputs = agent.inputs.length
			? agent.inputs
					.map(
						(i) =>
							`- **\`${i.name}\`**${i.required ? " _(required)_" : ""}: ${i.description ?? ""}`,
					)
					.join("\n")
			: "_None_";

		const workflow = agent.workflow
			.map(
				(w, i) =>
					`${i + 1}. ${w.step}${w.ref ? `\n   > ref: \`${w.ref}\`` : ""}`,
			)
			.join("\n");

		const policies = agent.policies.length
			? agent.policies
					.map((p) => `- \`${BUNDLE_DIR}/policies/${p}.md\``)
					.join("\n")
			: "";

		const outputTemplate = agent.output_template
			? `${BUNDLE_DIR}/templates/${basename(agent.output_template)}`
			: undefined;

		return `---
name: ${agent.id}
description: >-
  [SDLC:${agent.phase}] ${agent.description.trim().replace(/\n/g, " ")}
model: ${MODEL_MAP[agent.model_hint] ?? "claude-sonnet-4-6"}
tools:
${toolsYaml}
---

# ${agent.id} (phase: ${agent.phase})

${agent.description.trim()}
${claudeNote}
## Inputs

${inputs}

## Workflow

${workflow}
${outputTemplate ? `\n## Output\n\nFollow template \`${outputTemplate}\`.\n` : ""}${policies ? `\n## Policies\n\n${policies}\n` : ""}
---
_Generated by sdlc-agents. Do not edit — run \`sdlc build\` to regenerate._
`;
	}
}

function yamlQuoted(value: string): string {
	return JSON.stringify(value);
}

// Last path segment. Agent references always use forward slashes (e.g.
// "templates/prd.md"), so a plain split is correct and platform-independent.
function basename(p: string): string {
	return p.split("/").pop() ?? p;
}
