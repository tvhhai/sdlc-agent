import type { BuildContext } from "@sdlc-agents/core";
import { describe, expect, it } from "vitest";
import { parse as parseYaml } from "yaml";
import { ClaudeCodeAdapter } from "../index.js";

const adapter = new ClaudeCodeAdapter();

const mockCtx: BuildContext = {
	config: {
		targets: ["claude-code"],
		rootDir: "/project",
		variables: {},
		agentsDir: "agents",
	},
	templates: new Map(),
	policies: new Map(),
};

const mockAgent = {
	id: "code-reviewer",
	version: "1.0.0",
	phase: "review" as const,
	description: "Reviews PR diff against checklists.",
	model_hint: "high-reasoning" as const,
	model_variants: {
		claude: { prompt_append: "Use extended thinking." },
	},
	tools_required: ["read_file", "grep"],
	inputs: [{ name: "pr_diff", required: true, description: "The diff" }],
	workflow: [{ step: "Read diff" }, { step: "Run security checklist" }],
	policies: ["security-checklist"],
};

describe("ClaudeCodeAdapter", () => {
	it("has name 'claude-code'", () => {
		expect(adapter.name).toBe("claude-code");
	});

	it("renders an agent file and a command file per agent", () => {
		const outputs = adapter.render([mockAgent], mockCtx);
		expect(outputs.map((o) => o.path)).toEqual([
			".claude/agents/code-reviewer.md",
			".claude/commands/code-reviewer.md",
		]);
	});

	it("command file dispatches to the subagent and carries the SDLC prefix + footer", () => {
		const command = adapter
			.render([mockAgent], mockCtx)
			.find((o) => o.path === ".claude/commands/code-reviewer.md")!;
		const fm = parseYaml(command.content.split("---")[1]);
		expect(fm.description).toMatch(/^\[SDLC:review\] /);
		expect(command.content).toContain("Use the **code-reviewer** subagent");
		expect(command.content).toContain("$ARGUMENTS");
		expect(command.content).toContain("Generated by sdlc-agents. Do not edit");
	});

	it("output has valid YAML frontmatter with name and model fields", () => {
		const { content } = adapter.render([mockAgent], mockCtx)[0];
		expect(content).toMatch(/^---\n/);
		expect(content).toContain("name: code-reviewer");
		expect(content).toContain("model: claude-opus-4-8");
	});

	it("includes workflow steps in body", () => {
		const { content } = adapter.render([mockAgent], mockCtx)[0];
		expect(content).toContain("Read diff");
		expect(content).toContain("Run security checklist");
	});

	it("includes claude-specific prompt_append note", () => {
		const { content } = adapter.render([mockAgent], mockCtx)[0];
		expect(content).toContain("Use extended thinking.");
	});

	it("references the bundled support path, not the root policies/ dir", () => {
		const { content } = adapter.render([mockAgent], mockCtx)[0];
		expect(content).toContain("`.claude/sdlc/policies/security-checklist.md`");
		expect(content).not.toContain("`policies/security-checklist.md`");
	});

	it("bundles referenced templates + policies under .claude/sdlc when present in context", () => {
		const ctx: BuildContext = {
			...mockCtx,
			templates: new Map([["prd.md", "# PRD template body"]]),
			policies: new Map([
				["security-checklist.md", "# Security checklist body"],
			]),
		};
		const agent = { ...mockAgent, output_template: "templates/prd.md" };
		const outputs = adapter.render([agent], ctx);
		const support = outputs.filter((o) => o.path.startsWith(".claude/sdlc/"));
		expect(support.map((o) => o.path)).toEqual([
			".claude/sdlc/templates/prd.md",
			".claude/sdlc/policies/security-checklist.md",
		]);
		expect(support.find((o) => o.path.endsWith("prd.md"))!.content).toBe(
			"# PRD template body",
		);
	});

	it("skips support files absent from the build context (no dangling emit)", () => {
		// mockCtx has empty maps — only the agent + command files should appear.
		const outputs = adapter.render([mockAgent], mockCtx);
		expect(outputs.filter((o) => o.path.startsWith(".claude/sdlc/"))).toEqual(
			[],
		);
	});

	it("matches snapshot", () => {
		const outputs = adapter.render([mockAgent], mockCtx);
		expect(outputs[0].content).toMatchSnapshot();
	});

	it("escapes YAML frontmatter fields", () => {
		const agent = {
			...mockAgent,
			description: 'Reviews PR diff for the "core" team.',
			tools_required: ["read:file", "grep # exact"],
		};
		const { content } = adapter.render([agent], mockCtx)[0];
		const frontmatter = content.split("---")[1];
		const parsed = parseYaml(frontmatter);

		expect(parsed.description).toBe(
			'[SDLC:review] Reviews PR diff for the "core" team.',
		);
		expect(parsed.tools).toEqual(["read:file", "grep # exact"]);
	});
});
